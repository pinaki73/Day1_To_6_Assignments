public class Default_Values_of_primitive_datatypes {


    static boolean a;
    static int b;
    static long c;
    static short d;
    static char e;

    public static void main(String[] args) {
        System.out.println("The default value of datatype boolean is " + a);
        System.out.println("The default value of datatype int is " + b);
        System.out.println("The default value of datatype long is " + c);
        System.out.println("The default value of datatype short is " + d);
        System.out.println("The default value of datatype char is " + e);
    }

}
